    private static string RewriteHlsPlaylistForStreamEndpoint(string playlist, HttpRequest request, Guid videoId)
    {
        var baseUrl = $"{request.Scheme}://{request.Host}{request.PathBase}";
        var segmentPrefix = $"{baseUrl}/api/videos/{videoId}/stream/";

        var sb = new StringBuilder(capacity: Math.Max(playlist.Length, 256));
        using var reader = new StringReader(playlist);
        string? line;
        while ((line = reader.ReadLine()) != null)
        {
            var trimmed = line.Trim();
            if (trimmed.Length == 0 ||
                trimmed.StartsWith("#", StringComparison.Ordinal) ||
                trimmed.StartsWith("http://", StringComparison.OrdinalIgnoreCase) ||
                trimmed.StartsWith("https://", StringComparison.OrdinalIgnoreCase) ||
                trimmed.StartsWith("//", StringComparison.Ordinal))
            {
                sb.AppendLine(line);
                continue;
            }

            // If it's already an app-relative URL, leave it alone.
            if (trimmed.StartsWith('/'))
            {
                sb.AppendLine(line);
                continue;
            }

            var segmentName = trimmed;
            var absolute = $"{segmentPrefix}{segmentName}";
            sb.AppendLine(absolute);
        }

        return sb.ToString();
    }

    private string ResolveAuthoritativeFilePath(string? filePath)
    {
        if (string.IsNullOrWhiteSpace(filePath))
        {
            return string.Empty;
        }

        if (Path.IsPathRooted(filePath))
        {
            return filePath;
        }

        return Path.GetFullPath(Path.Combine(_environment.ContentRootPath, filePath));
    }

    private bool CanPreviewNonPublicVideo(ClaimsPrincipal user, VideoDto video)
    {
        var role = user.FindFirstValue(ClaimTypes.Role);
        if (role == "moderator" || role == "admin")
        {
            return true;
        }

        var userIdClaim = user.FindFirstValue(ClaimTypes.NameIdentifier);
        if (userIdClaim == null)
        {
            return false;
        }

        return Guid.TryParse(userIdClaim, out var userId) && userId == video.AuthorId;
    }

    // IMPORTANT: Must be registered before `{id}/{segment}` otherwise "playback" is treated as a segment name.
    [HttpGet("{id}/playback")]
    public async Task<IActionResult> GetPlayback(Guid id)
    {
        var video = await _videoService.GetVideoByIdAsync(id);
        if (video == null)
        {
            return NotFound();
        }

        var baseUrl = $"{Request.Scheme}://{Request.Host}{Request.PathBase}";

        // Published catalog videos: HLS only, anonymous OK.
        if (string.Equals(video.Status, "approved", StringComparison.OrdinalIgnoreCase))
        {
            return Ok(new VideoPlaybackDto($"{baseUrl}/api/videos/{id}/stream", "hls"));
        }

        if (!CanPreviewNonPublicVideo(User, video))
        {
            return Unauthorized();
        }

        var uploadsPath = ResolveUploadsPath();
        var hlsPath = Path.Combine(uploadsPath, id.ToString(), "playlist.m3u8");
        if (System.IO.File.Exists(hlsPath))
        {
            return Ok(new VideoPlaybackDto($"{baseUrl}/api/videos/{id}/stream", "hls"));
        }

        var originalDbPath = await _videoService.GetVideoOriginalPathAsync(id);
        var originalPath = ResolveAuthoritativeFilePath(originalDbPath);
        if (string.IsNullOrWhiteSpace(originalPath) || !System.IO.File.Exists(originalPath))
        {
            return NotFound();
        }

        return Ok(new VideoPlaybackDto($"{baseUrl}/api/videos/{id}/original", "file"));
    }

    [Authorize]
    [HttpGet("{id}/original")]
    public async Task<IActionResult> StreamOriginal(Guid id)
    {
        var video = await _videoService.GetVideoByIdAsync(id);
        if (video == null)
        {
            return NotFound();
        }

        if (!CanPreviewNonPublicVideo(User, video))
        {
            return Forbid();
        }

        var originalDbPath = await _videoService.GetVideoOriginalPathAsync(id);
        var path = ResolveAuthoritativeFilePath(originalDbPath);
        if (string.IsNullOrWhiteSpace(path) || !System.IO.File.Exists(path))
        {
            return NotFound();
        }

        var ext = Path.GetExtension(path).ToLowerInvariant();
        var mime = ext switch
        {
            ".mp4" => "video/mp4",
            ".webm" => "video/webm",
            ".mkv" => "video/x-matroska",
            ".mov" => "video/quicktime",
            _ => "application/octet-stream"
        };

        var fileBytes = await System.IO.File.ReadAllBytesAsync(path);
        return File(fileBytes, mime);
    }
    
    [HttpGet("{id}/stream/{segment}")]
    public async Task<IActionResult> GetSegment(Guid id, string segment)
    {
        try
        {
            var video = await _videoService.GetVideoByIdAsync(id);
            if (video == null) return NotFound();
            
            var uploadsPath = ResolveUploadsPath();
            var segmentPath = Path.Combine(uploadsPath, id.ToString(), segment);
            
            if (!System.IO.File.Exists(segmentPath))
                return NotFound();
            
            var fileBytes = await System.IO.File.ReadAllBytesAsync(segmentPath);
            return File(fileBytes, "video/MP2T");
        }
        catch
        {
            return NotFound();
        }
    }

    [HttpGet("{id}/{segment}")]
    public async Task<IActionResult> GetSegmentFromRoot(Guid id, string segment)
    {
        return await GetSegment(id, segment);
    }
    
    [Authorize]
    [HttpPost]
    [RequestSizeLimit(2L * 1024 * 1024 * 1024)]
    [RequestFormLimits(MultipartBodyLengthLimit = 2L * 1024 * 1024 * 1024)]
    public async Task<IActionResult> UploadVideo([FromForm] VideoUploadRequest request)
    {
        var userIdClaim = User.FindFirst(ClaimTypes.NameIdentifier)?.Value;
        if (userIdClaim == null) return Unauthorized();
        
        var userId = Guid.Parse(userIdClaim);
        var uploadsPath = ResolveUploadsPath();
        
        Directory.CreateDirectory(uploadsPath);
        
        var result = await _videoService.UploadVideoAsync(
            userId,
            new VideoCreateRequest(request.Title, request.Description, request.Category, request.Tags ?? new List<string>()),
            request.File,
            request.PreviewFile,
            uploadsPath
        );
        
        return Ok(result);
    }
    
    [Authorize]
    [HttpPut("{id}")]
    public async Task<IActionResult> UpdateVideo(Guid id, [FromBody] VideoUpdateRequest request)
    {
        var userIdClaim = User.FindFirst(ClaimTypes.NameIdentifier)?.Value;
        if (userIdClaim == null) return Unauthorized();
        
        var userId = Guid.Parse(userIdClaim);
        var success = await _videoService.UpdateVideoAsync(id, userId, request);
        
        if (!success) return NotFound();
        return Ok();
    }
    
    [Authorize]
    [HttpDelete("{id}")]
    public async Task<IActionResult> DeleteVideo(Guid id)
    {
        var userIdClaim = User.FindFirst(ClaimTypes.NameIdentifier)?.Value;
        if (userIdClaim == null) return Unauthorized();
        
        var userId = Guid.Parse(userIdClaim);
        var success = await _videoService.DeleteVideoAsync(id, userId);
        
        if (!success) return NotFound();
        return Ok();
    }
}

