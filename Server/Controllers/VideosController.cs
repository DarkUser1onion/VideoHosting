    private string ResolveUploadsPath()
    {
        var configured = _configuration["UploadsPath"] ?? "uploads";
        return Path.IsPathRooted(configured)
            ? configured
            : Path.GetFullPath(Path.Combine(_environment.ContentRootPath, configured));
    }
    
    [HttpGet]
    public async Task<IActionResult> GetVideos([FromQuery] string? search, [FromQuery] string? category)
    {
        var videos = await _videoService.GetAllVideosAsync(search, category);
        return Ok(videos);
