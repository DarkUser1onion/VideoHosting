    [HttpGet("{id}")]
    public async Task<IActionResult> GetVideo(Guid id)
    {
        var video = await _videoService.GetVideoByIdAsync(id);
        if (video == null) return NotFound();
        
        // Просмотров не увеличиваем здесь - только при реальном просмотре
        return Ok(video);
    }
    
    [HttpGet("{id}/preview")]
    public async Task<IActionResult> GetPreview(Guid id)
    {
        var video = await _videoService.GetVideoByIdAsync(id);
        if (video == null) return NotFound();
        
        var uploadsPath = ResolveUploadsPath();
        var videoFolder = Path.Combine(uploadsPath, id.ToString());
        var previewPath = Path.Combine(videoFolder, "preview.jpg");

        if (!System.IO.File.Exists(previewPath) && Directory.Exists(videoFolder))
        {
            var candidates = Directory.GetFiles(videoFolder, "preview.*");
            if (candidates.Length > 0)
            {
                previewPath = candidates[0];
            }
        }
        
        if (!System.IO.File.Exists(previewPath))
        {
            return File(PlaceholderPreviewBytes, "image/png");
        }
        
        var fileBytes = await System.IO.File.ReadAllBytesAsync(previewPath);
        var ext = Path.GetExtension(previewPath).ToLowerInvariant();
        var mimeType = ext switch
        {
            ".png" => "image/png",
            ".webp" => "image/webp",
            _ => "image/jpeg"
        };

        return File(fileBytes, mimeType);
    }
    
    [HttpGet("{id}/stream")]
    public async Task<IActionResult> StreamVideo(Guid id)
    {
        try
        {
            var hlsPath = await _videoService.GetVideoStreamPathAsync(id);
            
            // Просмотров не увеличиваем здесь - только при реальном просмотре через /watch endpoint
            
            // HLS playlists generated by ffmpeg typically reference segments with relative paths like `playlist0.ts`.
            // Clients often fetch the playlist from a URL like `/api/videos/{id}/stream` (no trailing slash).
            // RFC3986 relative resolution then points segments at `/api/videos/{id}/playlist0.ts`, but this API serves
            // segments at `/api/videos/{id}/stream/{segment}` — resulting in 404s and "black screen" in embedded players.
            //
            // Fix: rewrite relative segment lines to absolute URLs under `/api/videos/{id}/stream/`.
            var playlistText = await System.IO.File.ReadAllTextAsync(hlsPath);
            var rewritten = RewriteHlsPlaylistForStreamEndpoint(playlistText, Request, id);
            return Content(rewritten, "application/vnd.apple.mpegurl", Encoding.UTF8);
        }
        catch (FileNotFoundException)
        {
            return NotFound();
        }
    }
    
    [HttpPost("{id}/view")]
    public async Task<IActionResult> RecordView(Guid id)
    {
